void	ft_putnbr(int n)
{
	long	nb;

	nb = n;
	if (nb >= 0 && nb <= 9)
		ft_putchar(nb);
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr((nb % 10) + 48);
	}
}
